package j05_Assaignment_Atama_Operators;

public class C01_AtamaOperatorü {
    public static void main(String[] args) {

        //    =         x = 8      x = 8
        //    +=       x += 3     x = x + 3
        //    -=       x -= 3     x = x - 3
        //    *=       x *= 3     x = x * 3
        //    /=       x /= 3     x = x / 3
        //    %=       x %= 3     x = x % 3

        int yas=33;
        System.out.println(yas+5); //38 -> atama (=) olmadığı için yaş hala 33
        System.out.println("yas ="+ yas); //yas=33
        System.out.println(yas += 11); //44 atama yapıldığı için yaş 44 olur










    }
}
